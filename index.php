          <h3>Multi-Path Video</h3>

          <p>User-generated video customization is where a user chooses video clips in any order and sequences them to create a shareable custom video. This is straight forward on a desktop where bandwidth is generally not an issue and videos can just be served and auto-played one right after another. Bandwidth aside, there are issues with doing this on mobile devices; in particular, within iOS. Due to the limitations of the Apple mobile operating system, users are required to tap a play button for videos to begin playing. This creates a less than ideal experience if the user has to click play at the start of each segment within the customized video.</p>

          <p>One solution is to use very high-powered media web servers, which can generate individual video streams on the fly to multiple users. The approach is effective and used by many of the larger players â€“ it's not a practical solution for limited or even mid-range budgets. Usage is charged by the bandwidth and limited in the amount of simultaneous viewers. If the site becomes viral or simply popular, clients can find themselves in the position of being forced to shut the site down simply due to hosting costs.</p>

          <p>Another solution is to create every possible permutation of video that can be created. In the case of our project, we had users select one of thee sets of 6 videos - 18 videos in all - for a total of 360 possible permutations. Aside from the huge amount of hours it would take someone to set up and render all this out, there would inevitably be mistakes that would be very difficult to ferret out during Q&A.</p>

          <p>Our solution was to actually invent a software application that can automatically assemble every possible permutation. It does what the high-powered web host server would do every time there was a request, but this way it's done only once on our local server prior to even launching the site. So when the user thinks they're creating their own custom arrangement, they are simply selecting one of the hundreds of pre-created videos. The result is a fast, seamless experience on
          any platform.</p>
