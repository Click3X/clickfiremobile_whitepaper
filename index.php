          <h3>Consistent UX</h3>

          <p>User experience and information architecture have always been key to any successful application, and are even more important when planning multi-platform applications. Our team includes an expert in cross-channel user experience, and before any technology or design is defined, they strategize with both teams to develop wireframes detailing the optimal architecture for every possible platform.</p>

          <p>Consider a restaurant website. When creating the experience for the desktop, we may draw upon the convention of putting the phone number, hours of operation and address in the footer. Users have been trained and conditioned to look for things there over the years of looking at such similar websites. However, on a mobile device, the paradigm changes. Users looking up a restaurant website on a mobile device are more likely to be looking for contact and address information. Therefore, in a mobile environment, that information should be brought to the forefront, without the need for menus and scrolling, but geo-location drives to specific directions and maps with a phone call or a
          single click.</p>

