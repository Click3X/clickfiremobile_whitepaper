          <h3>Adaptive and Responsive Design</h3>

          <p>In a nutshell, responsive design is basically the adjusting of content's appearance or presentation layer so that it "responds" to different screen sizes, and is typically done using CSS. Adaptive design is the changing of content or behavior to accommodate the current channel/device, which can be done using JavaScript or CSS media queries. Both of these strategies seek to achieve a similar goal.</p>

          <p>Performed properly, these techniques can produce many layouts of the same site â€“ up to eight for each page! However, this upfront work is necessary to ensure a consistent user experience that is not only optimized for the screen it is being viewed on, but also for the environment the site is being used in. Combined with the user experience exercises, our developers are given a well thought out base to apply our cutting edge technology.</p>
          <img src="img/screens.jpg" width="100%" />
          <p>Given the vast number of mobile devices available, determining which screen resolutions to design for can be a bit daunting. There are always exceptions to any method of abstraction; however, we've found that the chart above of screen widths will generally cover the majority of use case scenarios. Notice that only the screen widths are listed because in most cases that's the greatest area of concern with regards to UX.</p>

          <p>Interactive users have generally grown accustomed to scrolling vertically to view content overflow. In some exceptional cases however; we do provide design frames for specific screen heights as well. In the case of the restaurant website, we'd most likely want the footer containing the contact information to be visible at all times, which would require further evaluating the vertical real-estate available on the device being used.</p>

          <p>Obviously even the most insightful design and user experience principles require the best possible technology. ClickFire Media has consistently been at the forefront of digital programming techniques and we bring that experience to bear in the mobile sphere as well. For our web applications, we primarily use HTML5 with integrated responsive frameworks such as Twitter-Bootstrap, Response JS, and our own library of extensible HTML/JavaScript components.</p>

          <p>To illustrate our usage of some of these technologies, we'll continue with the restaurant website example. When visited on a desktop, navigation typically appears horizontally laid out across the top or vertically stacked down the left side. When the screen size width drops down into a mobile range, Twitter-Bootstrap automatically collapses the main navigation into a full-width pull-down menu complete with a nice animation transition. A collapsed navigation would benefit the mobile user by neatly stacking the navigation out of view making the most helpful content immediately visible upon page load. Furthermore, Response JS could be utilized to display the restaurant's contact/location information in the header container, rather than the footer. To ensure cross-browser compatibility we also utilize tools like Modernizr, Initializr, and jQuery UI.</p>
